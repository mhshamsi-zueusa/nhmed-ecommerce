<p><strong>E</strong>ach year, thousands of unsuspecting seniors sustain deadly slip and fall incidents. Today, falls are the second leading cause of injury-related deaths for senior citizens from ages 65 to 84. Although the risks associated with falling are grave, there are steps that seniors and their caregivers can take to prevent injuries before they occur.</p>



