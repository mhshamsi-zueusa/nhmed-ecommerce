<p>As a result, there are countless bodily risks associated with falls. Some of the most common include <strong>broken bones or fractures, brain injuries, spinal cord injuries</strong>, hip fractures, and more.</p>



<p>Hip fractures are especially immobilizing, as half of all seniors 65 and older with a hip injury are never able to return home from hospital care. Likewise, 87% of bone fractures for individuals 65 years or older are caused by falls.</p>



